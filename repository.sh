#####################################################################################################################################################
"


espaciador(){
       	echo "<---------------------------------->"
}

archivo(){ 
	touch README.md
	echo "primer commit del repositorio" >> README.md`
}

commit(){ 
	git add . -A
       	git commit -m "$1"`
}

directorio(){
	mkdir $1 && cd $1/
}

rama(){
	git branch $1 && git checkout $1/`
}


echo "多Quieres crear un nuevo repositorio?" ; read respuesta

if [[ $respuesta = "si" ]]
then
	echo "Creando repositorio..."
	espaciador
	echo "Nombre del repositorio: " ; $directorio ; git init . && $archivo
	espaciador	
	echo "Nombre del commit: " ; commit "
	echo $espaciador
fi

echo "多Que deseas hacer?" ; read opcion

case $opcion in
	"actualizar")
		echo "多Desde donde quieres actualizar? <rama> <directorio>"
		$espaciador
		read movimiento ; read carpeta
		git checkout $movimiento && cd $carpeta ; $commit
		git show HEAD
	;;
	"clonar")
		echo "en mantenimiento..."
	;;
	"eliminar")
		echo "en mantenimiento..."
	;;
	"restaurar")
		echo "en mantenimiento..."
	;;
	"remoto")
		echo "en mantenimiento..."
	;;
	"ramas")
		echo "多Cuantas ramas vas a crear?"
		read numero
		echo $espaciador
		while [ $numero -ge 0 ]
		do
			echo "Escribe el nombre de la rama"
			echo $espaciador
			$rama && $commit; git checkout master
			echo $espaciador
			git branch -av
			numero= $numero-1
		done
	;;
	*)
		echo 'Opcion no correcta'
	;;
esac








